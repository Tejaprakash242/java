package Programing;

public class LogicalOperators {
	public static void main(String[] args) 
	{
		int x = 18;
		int y = 99;
		int a = 15;
		int b = 99;
		System.out.println(x>y);
		System.out.println(a<b);
		System.out.println(x>y && a<b);
		System.out.println(x>y || a<b);
		System.out.println(!(x>y && a<b));
		
	}

}
